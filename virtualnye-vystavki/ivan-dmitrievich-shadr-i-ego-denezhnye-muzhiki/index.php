<p>
	 &nbsp;&nbsp;«Рабочий», «Крестьянин», «Красноармеец» и «Сеятель» – не единственные работы, выполненные Шадром в период сотрудничества с Гознаком. Так, в 1923 году он подготовил как минимум два варианта государственного герба СССР, не получившие утверждения. В то же время на основе скульптур рабочего и крестьянина Шадр создал гипсовый барельеф – проект будущих советских монет. Однако утвержден он не был. В 1923 году действительно разрабатывалось оформление будущих монет СССР, но это делалось без участия Гознака.<br>
	 &nbsp;&nbsp;Летом того же 1923 года Шадр выполнил несколько работ для Всероссийской сельскохозяйственной и кустарно-промышленной выставки. Это барельеф «Борьба с землей», фигура рабочего-металлиста и «эмблема СССР в виде фигуры женщины, выбивающейся из скалы капитализма». В Отчете Технического отдела Гознака помещена ее фотография с примечанием о том, что это – первоначальный вариант, перерабатываемый скульптором. <br>
</p>
