<p>
	 &nbsp;&nbsp;В собрании Гознака сохранилось немало материалов, связанных с работой Шадра, – гипсовые копии скульптур («Рабочий», «Красноармеец», «Крестьянин»), их фотографии, сделанные при разработке денежных знаков, эскизы и образцы продукции Гознака, фотографии мастерской Шадра, которая находилась напротив здания 2-й Московской фабрики Гознака на Мытной улице.<br>
	 &nbsp;&nbsp;Идея передать облик человека на банкноте посредством изображения скульптуры была не новой. Еще в 1895 году скульптурный портрет Петра I появился на одном из эскизов «императорской серии» Р. И. Рёсслера и впоследствии был «перенесен» на 500-рублевую банкноту образца 1898 года. Однако в 1920-е годы эта идея наполнилась новым содержанием: теперь на банкнотах и других государственных бумагах планировалось показать новых хозяев жизни – через их скульптурные портреты. Именно противопоставление старых банкнот и марок с портретами императоров и новых с изображениями простых людей подчеркивается едва ли не во всех публикациях, связанных с историей создания советских банкнот 1920-х годов. Уже в отчете Технического отдела Гознака за январь – сентябрь 1922 года скульптурам Шадра дана высокая оценка как символу «идеи рабоче-крестьянской власти». В более позднем отчете Гознака отмечено, что «теплый прием, оказанный этим фигурам в художественных сферах и в широких массах пролетариата, послужил гарантией, что идея взята правильно».<br>
	 &nbsp;&nbsp;Существуют многочисленные публикации по истории создания скульптур, в которых раскрываются обстоятельства работы Шадра над ними и называются их прототипы. Широко известны воспоминания о том, как Шадр искал модели для скульптур среди крестьян (прототипом крестьянина был П. П. Калганов, сеятеля – К. К. Авдеев). <br>
	 &nbsp;&nbsp;Уже в 1924 году историк В. П. Бирюков опубликовал в журнале «Шадринское научное хранилище» заметку о работе Шадра, основанную на своих беседах с «денежными мужиками» (так их стали называть односельчане) – прототипами скульптур. По его мнению, сеятель – это, с одной стороны, «глубоко реалистическая фигура», а с другой – «вовсе не простой сеятель, а целый символ всего крестьянства». <br>
	 &nbsp;&nbsp;В том же году краткая заметка о «шадринских» крестьянах появилась на страницах журнала «Советский филателиста». Автор – самарский филателист Ф. Т. Яковлев – дал интересную характеристику скульптур: «фигура рабочего производит какое-то странное впечатление – она вся из глубоких складок кожи и мускулов. Это, по объяснению И[вана] Д[митриеви]ча [Шадра], рабочий не русский, не английский, не немецкий, а есть символ международного рабочего, содержащего в себе разноплеменные черты. Фигура красноармейца вполне национальна. Изображает она 20-летнего Ванюху, с полуоткрытым ртом, совершенно еще не искушенного жизнью, а живущего казармой». Далее автор свидетельствует, что «красноармеец вышел до того удачен, что т. Троцкий (разрядка в источнике – А. Б.) в Москве, подойдя к фигуре, потрепал ее по щеке и сделал одобрительное замечание». <br>
	 &nbsp;&nbsp;Троцкий был не единственным, кому понравились скульптуры. Например, известный литературовед-марксист В. М. Фриче отозвался о скульптуре рабочего с «величайшей похвалой, признав за нею громадную художественную ценность». По иронии судьбы в 1931 году именно Шадр выполнил проект надгробного памятника Фриче, скончавшегося в 1929 году. <br>
	 &nbsp;&nbsp;Однако в адрес скульптур звучала и критика. Наиболее ярко она выражена в статье филателиста Д. Б. Валерона, который посвятил разбору скульптур Шадра три страницы. По его мнению, крестьянин Шадра – «ветхозаветный лубочный пейзанин, нисколько не похожий на тех крестьян, которые принимали участие в Октябрьской революции и на которых эта революция опирается теперь в деревне»; красноармеец – «неразвитый деревенский паренек, только что переодетый в красноармейскую форму, но не имеющий по существу ничего общего ни с военной дисциплиной и порядком (расстегнутый ворот), ни с психикой закаленного красного бойца, каким должен быть красноармеец». Скульптура рабочего подвергнута схожей уничтожающей критике. Итоговый вывод неутешителен: «все три произведения Шадра выпячивают исключительно первобытные антропологические черты в ущерб психическим и классовым. Глядя на эти бюсты, невольно приходишь к заключению, что в них скульптор далеко нелестно отобразил пролетариат, крестьянство и Красную армию. Приходится только удивляться, как все-таки мы ухватились за эти изображения и вот уже 7 лет помещаем их на государственные регалии и никак не можем с ними расстаться». Несмотря на излишнюю резкость, следует признать, что некоторая доля истины в этих словах есть. Возможно, поэтому в начале 1930-х годов «шадровские» скульптуры на «государственные регалии» помещать перестали. <br>
</p>
