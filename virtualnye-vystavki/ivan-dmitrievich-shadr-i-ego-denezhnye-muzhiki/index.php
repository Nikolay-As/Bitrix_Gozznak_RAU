<p>
	 &nbsp;&nbsp;Первые бумажные денежные знаки с изображением скульптур, созданных И. Д. Шадром, вышли в обращение в конце 1923 – начале 1924 года. Это были государственные денежные знаки СССР номиналом 15 и 25 тысяч рублей. Работа над их проектом началась в 1922 году. Изначально их эскизы были исполнены как эскизы денежных знаков достоинством 100 (с изображением крестьянина) и 500 (с изображением красноармейца) рублей. Скульптуры на них награвировал А. П. Троицкий, автором самих эскизов были А. Г. Якимченко и В. К. Куприянов. В этой же серии проектировалась купюра с изображением рабочего, но в итоге в обращение ее не выпустили. <br>
	 &nbsp;&nbsp;Во второй половине 1923 года банкноты были переделаны в связи с образованием СССР и инфляцией. Таким образы, номиналы денежных знаков возросли, а государственная принадлежность изменилась: они стали первыми банкнотами СССР.<br>
	 &nbsp;&nbsp;Первые банкноты с изображениями «шадровских» скульптур ходили очень недолго, поскольку вскоре после их выпуска начался новый этап денежной реформы, и изготовление этих купюр было прекращено.<br>
	 &nbsp;&nbsp;В 1924 году в обращение вышел билет достоинством 3 червонца с изображением «шадровского» сеятеля. Эта банкнота готовилась с конца 1923 года. Общую компоновку банкноты осуществил художник Я. Б. Дрейер. Изображение сеятеля было награвировано П. С. Ксидиасом по рисунку И. И. Дубасова, сделанному с фотографии скульптуры. Распространено заблуждение, что «шадровский» сеятель изображен на советском золотом червонце. Однако это не так: автором эскиза монеты был медальер Петроградского монетного двора А. Ф. Васютинский, использовавший собственный эскиз медали, выполненный еще до революции.<br>
	 &nbsp;&nbsp;В 1925 году в обращении появилась последняя банкнота с изображением «шадровской» скульптуры – 5 рублей. Предполагалось поместить на этот денежный знак портрет С. Н. Халтурина, В. И. Ленина или М. И. Калинина, но остановились в итоге на «шадровском» рабочем. Несмотря на то, что художники Гознака в 1920-е годы выполнили немало проектов банкнот с изображением шадровских «денежных мужиков», утверждены не были. Зато скульптуры появлялись на ценных бумагах – в частности, на обязательствах Центральной кассы Народного комиссариата финансов и облигациях хлебного займа.<br>
</p>
