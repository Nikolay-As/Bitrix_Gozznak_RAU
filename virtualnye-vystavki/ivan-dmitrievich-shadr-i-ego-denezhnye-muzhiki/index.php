<p>
	 &nbsp;&nbsp;Будущий скульптор родился в семье плотника Дмитрия Евграфовича Иванова 30 января (11 февраля) 1887 года. Его отец жил в городе Шадринск Пермской губернии. Позднее по названию города Шадр придумал себе псевдоним. <br>
	 &nbsp;&nbsp;С детства мальчик проявлял способности к рисованию. В 1901 году Иван Дмитриевич без подготовки сдал экзамен для поступления в Екатеринбургскую художественно-промышленную школу, где проучился до 1906 года. В 1907 году отправился путешествовать по России и пешком дошел до Санкт-Петербурга. Там его рисунки увидел знаменитый русский художник И. Е. Репин, который дал им высокую оценку. Затем Шадр учился в рисовальной школе Общества поощрения художеств, а в 1910 году отправился учиться в Париж и Рим. Вернувшись в Россию, учился в Петербургском археологическом институте, а в 1914–1917 годах работал у известного российского кинопромышленника А. А. Ханжонкова. В 1918 году уехал в Омск и оставался там до 1921 года, выполняя заказы колчаковских властей. Затем вернулся в Москву. <br>
	 &nbsp;&nbsp;В 1920–1930-е годы Шадр был успешным и известным советским скульптором, мастером ленинианы. Помимо его «денежных» работ, он известен как автор скульптур «Борьба с землей» (1923), «Булыжник – орудие пролетариата» (1927), «Девушка с веслом» (1934, 1936), памятника В. И. Ленину для Земо-Авчальской ГЭС (1927), памятника М. Горькому (1939). В 1930 году совместно со скульптором П. И. Таёжным Шадр создал модель первого варианта ордена Ленина.<br>
	 &nbsp;&nbsp;Умер скульптор в Москве 3 апреля 1941 года. <br>
</p>
