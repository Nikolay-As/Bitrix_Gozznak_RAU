<p>
	 &nbsp;&nbsp;<b>Выставка посвящена советскому скульптору-монументалисту Ивану Дмитриевичу Шадру (1887–1941). Его творчество напрямую связано с деятельностью Гознака: в 1922 году Шадр по заказу Гознака вылепил скульптуры рабочего, крестьянина, красноармейца и сеятеля – специально для того, чтобы поместить их изображения на денежные знаки, почтовые марки, платежные обязательства и другую продукцию. Сто лет назад – в 1923 году – были выпущены первые денежные знаки с изображениями скульптур работы И. Д. Шадра.</b>
</p>
