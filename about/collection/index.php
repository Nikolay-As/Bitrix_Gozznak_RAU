	</div>
	<div class="is-col is-60">
		<p>
			В конце XVIII в. было решено выпустить новые ассигнации, усложнив их оформление. В 1802 – 1803 гг. новые банкноты были отпечатаны, но в обращение они не выпускались, а хранились в Ассигнационном банке. Однако заменить старые ассигнации новыми было невозможно из-за технических трудностей, а тиражи ассигнаций старого образца во второй половине 1800-х гг. возросли. Поэтому новые ассигнации были сожжены, так и не дождавшись своего часа. До наших дней дошли лишь несколько этих банкнот, и они считаются величайшей редкостью. В собрании Гознака есть ассигнации этого образца разных номиналов, а также пробные оттиски.
		</p>
	</div>
</div>

