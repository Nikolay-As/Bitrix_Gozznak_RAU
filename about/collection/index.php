<h3>Петр I Алексеевич (1682 – 1725, император с 1721)</h3>

<p class="is-small">Рубль 1724 г. Серебро. Санкт-Петербургский (Коллежский) монетный двор. Медальер: А. Шульц (?). Диаметр: 43,5 мм. Вес: 28,31 г. Гурт: шнуровидная насечка.</p>
<p>Перед вами – одна из первых монет, выпущенных в Санкт-Петербурге через три года после того, как Петр I стал императором. Этот рубль равен талеру – международной торговой монете того времени. Подобные монеты стали чеканиться в России в начала XVIII века.
</p>

