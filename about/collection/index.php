<h3>Анна Иоанновна (1730 – 1740)</h3>

<p class="is-small">Рубль 1736 г. Серебро 802°. Санкт-Петербургский монетный двор (в бывших палатах кн. Прозоровского). Медальер: И.-К. Хедлингер. Диаметр: 41 мм. Вес: 25,35 г. Гурт: узорный орнамент</p>
<p>
	В 1736 году в Россию был приглашен знаменитый европейский медальер И.-К. Хедлингер, который, в числе прочего, исполнил штемпели серебряного рублевика с очень выразительным мастерски исполненным портретом императрицы. В Петербурге, в бывших палатах князя Прозоровского (монетный двор в Петропавловской крепости до 1738 года был закрыт) этими штемпелями было сделано всего 2671 рублевая монета. Эти монеты сделаны из первого российского серебра, добытого в количестве нескольких пудов на острове Медвежий.
</p>

