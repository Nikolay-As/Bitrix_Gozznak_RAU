<h3>Государственная ассигнация достоинством 100 рублей образца 1802 г.</h3>

<div class="is-row lightgallery is-stack-40">
	<div class="is-col is-40">
		<a class="gallery-item" href="/images/collection/bonistica/big/2.png">
			<img src="/images/collection/bonistica/small/2.png" alt="">
