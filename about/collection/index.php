<h3>СССР</h3>

<p class="is-small">100 рублей 1970 г. (памятные). Золото 900°. Ленинградский монетный двор. Диаметр: 50 мм. Вес: 103,19 г. Гурт: рубчатая насечка. </p>
<p>
	Самая массовая памятная монета СССР была выпущена к 100-летию со дня рождения В. И. Ленина, отмечавшемуся в 1970 году. Это был рубль, отчеканенный в медно-никелевом сплаве. Однако в период подготовки юбилейных торжеств велась разработка памятных монет номиналом больше рубля. Свидетельство тому – множество эскизов, сохранившихся в фондах Гознака комплект пробных золотых монет достоинством 10, 25, 50 и 100 рублей. Они были отчеканены в 1969 году, но не были утверждены к массовому выпуску.
</p>

