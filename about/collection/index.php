	</div>
	<div class="is-col is-60">
		<p>
			В январе 1769 года в России по указу императрицы Екатерины II, были выпущены первые российские бумажные денежные знаки – ассигнации. Печатались они в Сенатской типографии в Петербурге, а бумага для них отливалась на фабрике в Красном селе. Печатались ассигнации номиналом 25, 50, 75 и 100 рублей. Они разменивались на медные монеты. Ассигнации первых лет выпуска – исключительная редкость. До наших дней таких банкнот дошли считанные единицы. В собрании Гознака их две – 25 рублей 1769 и 100 рублей 1779 года.
		</p>
	</div>
</div>

