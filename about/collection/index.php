<h3>Николай II Александрович (1894 – 1917)</h3>

<p class="is-small">37 рублей 50 копеек – 100 франков 1902 г. Золото 900°. Санкт-Петербургский монетный двор. Медальеры: А. Ф. Васютинский, А. А. Грилихес (отец). Диаметр: 33,5 мм. Вес: 32,33 г. Гурт: надпись «ЧИСТАГО ЗОЛОТА 6 ЗОЛОТНИКОВ 77,4 ДОЛИ ▪»</p>
<p>
	На монетах Николая II портрет повернут влево, что противоречит российской традиции, зато согласуется с английской, где каждый последующий монарх на монетах «смотрит» в противоположную сторону. Монета с самым экзотическим в российской истории номиналом – 37 рублей 50 копеек – 100 франков – относится к донативным, предназначавшимся для подарков высокопоставленным лицам. Ее тираж – немногим более 200 экземпляров. Большинство таких монет были розданы членам французской правительственной делегации, прибывшей во главе с президентом Э. Лубе в Санкт-Петербург в мае 1902 года.
</p>

