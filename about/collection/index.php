<h3>СССР</h3>

<p class="is-small">Один червонец 1925 г. Золото 900°. Ленинградский монетный двор. Диаметр: 22,6 мм. Вес: 8,27 г. Гурт: надпись «ЧИСТОГО ЗОЛОТА 7,74234 ГРАММА (П.Л.)»</p>
<p>
	Одним из мероприятий денежной реформы, проведенной в Советской России в 1922 – 1924 годах стал выпуск новых золотых монет – червонцев. В 1923 году были отчеканены червонцы с гербом РСФСР. В том же году, после образования СССР был разработан рисунок общесоюзного герба, что повлекло за собой изменение оформления монет. Однако червонцы, датированные 1925 годом, в обращение так и не попали.
</p>

