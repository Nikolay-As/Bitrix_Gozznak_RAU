<h3>Петр II Алексеевич (1727 – 1730)</h3>

<p class="is-small">Червонец 1729 г. Золото 980°. Московский (Красный) монетный двор. Диаметр: 22 мм. Вес: 3,45 г. Гурт: гладкий</p>
<p>
	Червонцами в России называли дукаты – европейские золотые торговые монеты. При Петре I начали выпуск русских дукатов-червонцев для платежей за границей. Перед вами – червонец, выпущенный небольшим тиражом в короткое правление Петра II.
