<h3>Петр III Федорович (1761 – 1762)</h3>

<p class="is-small">Рубль 1762 г. Серебро 802°. Санкт-Петербургский монетный двор. Медальер: С. Ю. Юдин. Диаметр: 39,5 мм. Вес: 26,09 г. Гурт: надпись «С. ПЕТЕРБУРХСКАГО МОНЕТНАГО ДВОРА».</p>
<p>
	На серебряных рублях Петра I и Петра II помещалась крестообразная композиция из букв «П» и цифр «I» или «II». В 1762 году, когда на престол вступил Петр III, был отчеканен пробный рубль аналогичного оформления. Штемпели для него резал С. Ю. Юдин – один из самых талантливых русских медальеров той эпохи. Однако этот вариант был забракован, и на реверсе монет остался гербовый орел. Пробные рублевики 1762 года – одни из самых редких монет России XVIII века.</p>

