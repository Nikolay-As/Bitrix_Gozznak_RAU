<h3>Елизавета Петровна (1741 – 1761)</h3>

<p class="is-small">Рубль 1757 г. Серебро 802°. Санкт-Петербургский монетный двор. Медальер: Ж.-А. Дасье. Диаметр: 39,5 мм. Вес: 26,62 г. Гурт: надпись «С.-ПЕТЕРБУРХСКОГО МОНЕТНАГО ДВОРА».</p>
<p>
	В 1750-е годы в Россию был приглашен медальер Ж.-А. Дасье. Он создал портреты императрицы для серебряных рублей и золотых империалов и новый рисунок гербового орла. Портрет работы Дасье продержался на монетах лишь один год (1757-й), а орел использовался и позже.
</p>

