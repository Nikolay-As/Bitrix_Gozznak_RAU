<h3>Иван VI Антонович (1740 – 1741)</h3>

<p class="is-small">Рубль 1741 г. Серебро 802°. Санкт-Петербургский монетный двор. Диаметр: 43,5 мм. Вес: 25,51 г. Гурт: узорный орнамент.</p>
<p>
	В 1740 году на российском престоле оказался младенец Иван Антонович. Уже через год он был свергнут дочерью Петра Великого Елизаветой и погиб в тюрьме в 1764 году. Интересно, что на монетах Иван Антонович назван «Иоанном III», считая от первого российского царя Ивана Грозного. Монеты чеканились до переворота Елизаветы Петровны, а позже изымались из обращения по политическим соображениям. За время правления Елизаветы было издано четыре указа об их изъятии, и каждый раз наказание за «утайку» этих монет ужесточалось.
</p>

