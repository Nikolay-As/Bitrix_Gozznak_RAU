	<h3>
		Алексей Михайлович (1645 – 1676)
	</h3>
	<p class="is-small">
		Рубль 1654 г. Серебро. Московский (Новый) денежный двор.
	</p>
<p>
	В 1654 году в России началась денежная реформа, в ходе которой были отчеканены первые рублевые монеты. По указу царя на специально построенном Новом денежном дворе в Москве стали чеканить серебряные рубли и полуполтины и медные полтинники. По свидетельству современника, русские бояре целовали рубли с портретом царя, выражая свое почтение к государю. Однако новые рубли были неполноценными: они чеканились прямо на европейских серебряных талерах, которые содержали серебра не на 100, а только на 64 копейки. Очень скоро чеканка рублей была прекращена. До нас их дошло лишь около сорока таких монет.
</p>

