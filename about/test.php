<p>
	&nbsp;&nbsp;Медвежонок Гуртик – волшебный житель Музея истории денег, расположенного в Петропавловской крепости. В музее у него есть свой собственный домик. Имя медвежонка связано с деньгами и происходит от названия ребра монеты (гурта).<br>
	&nbsp;&nbsp;Гуртик любит путешествовать, посещать театры и музеи. В этот раз он решил отправиться в Ленинградский зоопарк, чтобы увидеть тех зверей и птиц, изображения которых встречаются на монетах и банкнотах.<br>
