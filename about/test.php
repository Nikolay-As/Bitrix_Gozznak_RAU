	&nbsp;&nbsp;<a name="metka2"><b>Лось</b></a>
</p>
<p>
	&nbsp;&nbsp;Лось – самый большой представитель семейства оленевых. Его часто называют «сохатым», потому что рога животного напоминают соху. Размеры рогов лося достигают 180 см, а их вес составляет до 30 кг. Осенью самцы сбрасывают рога, а весной отращивают новые.<br>
	&nbsp;&nbsp;Лоси предпочитают вести оседлый образ жизни и не путешествовать на большие расстояния без необходимости. Если в окрестностях достаточно пищи, то животное с большей долей вероятности проведет здесь всю свою жизнь.<br>
	&nbsp;&nbsp;Изображение лося представлено на рекламной банкноте Гознака, посвященной сохранению фауны центральной полосы России. Она отпечатана в 2016 году Пермской печатной фабрикой Гознака на полимерном носителе.<br>
	&nbsp;&nbsp;Также этому животному посвящены семь памятных монет из золота и серебра серии «Сохраним наш мир», отчеканенных в 2015 году на Московском и Санкт-Петербургском монетных дворах. Например, на 100-рублевой золотой памятной монете изображен лось, который поедает ветку дерева. Вес этой монеты – 15,72 г.
</p>
