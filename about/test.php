	&nbsp;&nbsp;<a name="metka4"><b>Заяц</b></a>
</p>
<p>
	&nbsp;&nbsp;Зайцы живут везде, кроме Австралии и Антарктиды. Зайцы ведут очень осторожный и скрытный образ жизни. Они плохо видят, но обладают отличным слухом.<br>
	&nbsp;&nbsp;На золотых и серебряной инвестиционных монетах, выпущенных в 2013 году, изображен один из талисманов Олимпийских игр в Сочи – «Зайка». На шее Зайки – большой бант. Особенность этих монет еще и в том, что они – прямоугольной формы. Номинал золотых монет – 50 и 100 рублей, серебряной – 3 рубля.<br>
	&nbsp;&nbsp;В 2018 году на памятных монетах появилось изображение еще одного Зайца – из мультфильма «Ну, погоди!». Эти 25-рублевые монеты вышли в серии «Российская (советская) мультипликация» в трех видах: серебряные монеты отчеканены на Санкт-Петербургском монетном дворе, а из медно-никелевого сплава (с цветным изображением и без него) – на Московском монетном дворе. 
</p>
