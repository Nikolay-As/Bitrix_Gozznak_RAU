<p>
	&nbsp;&nbsp;<a name="metka5"><b>Амурский тигр</b></a>
</p>
<p>
	&nbsp;&nbsp;Амурский тигр часто упоминается в сказках Дальнего Востока и является символом этого региона. Некоторые легенды рассказывают о том, что тигр находится в родстве с человеком, поэтому этому животному нужно оказывать знаки особого почтения. В Приморском крае ежегодно отмечают День тигра, стараясь привлечь внимание жителей к проблемам этого вида.<br>
	&nbsp;&nbsp;Изображение тигра «спрятано» на банкноте номиналом 5000 рублей, посвященной Хабаровску – крупнейшему городу Дальнего Востока. Чтобы найти на этой купюре тигра, нужно вооружиться лупой.<br>
	&nbsp;&nbsp;Амурский тигр неоднократно становился героем памятных монет. В 1992–1993 годах были выпущены памятные монеты серии «Красная книга»: 1-рублевая серебряная и 10-рублевая из нейзильбера и латуни. А в 1996 году амурскому тигру посвятили выпуск серии «Сохраним наш мир» из семи золотых и серебряных монет. В рамках этого выпуска, в частности, на Санкт-Петербургском монетном дворе была отчеканена серебряная 100-рублевая монета весом 1 кг 111 г с изображением амурского тигра на камне у водоема. На заднем плане монеты – изба и сибирский кедр.
</p>
