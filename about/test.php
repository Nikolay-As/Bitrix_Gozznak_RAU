<p>
	&nbsp;&nbsp;<a name="metka7"><b>Рысь</b></a>
</p>
<p>
	&nbsp;&nbsp;Рысь – самая северная кошка, живущая в глухих лесах. Расположенные на ее ушах кисточки улавливают малейшие колебания воздуха и помогают хорошо различать звуки. Рысь очень ловка: она не только прекрасно лазает по деревьям и скалам, но и быстро бегает, делает большие, до 3,5–4 метров прыжки, совершает длительные переходы, хорошо плавает.<br>
	&nbsp;&nbsp;Семейство рысей изображено на памятной серебряной монете номиналом 100 рублей серии «Сохраним наш мир». Мама-рысь и двое ее котят сидят на фоне ели. На заднем плане монеты представлены лошадь с жеребенком и фигуры людей, изготавливающих сруб. Вес этой монеты – 1 кг 111 г. На другой монете этой серии – золотой 100-рублевой, весом 15,72 г – хищная кошка представлена лежащей на дереве, на заднем плане виднеется древнерусский город. Она отчеканена в 1995 году на Санкт-Петербургском монетном дворе. Всего же в рамках серии «Сохраним наш мир» в 1995 году выпустили шесть памятных монет из золота и серебра, посвященных рыси. 
</p>
