<p>
	&nbsp;&nbsp;<a name="metka6"><b>Орел</b></a>
</p>
<p>
	&nbsp;&nbsp;Орлы – хищные птицы семейства ястребиных. Их часто изображали на гербах, печатях, монетах, живописных произведениях. Во многих мировых культурах встречается изображение не только одноглавого, но и двуглавого орла – древнего символа власти и силы. Он упоминается в шумерской мифологии, а его изображение использовали в Персии, странах Ближнего Востока и Европы. По одной из версий на Русь двуглавый орел «прилетел» из Византии.<br>
	&nbsp;&nbsp;На российских монетах двуглавый орел впервые появился более 500 лет назад – в конце XV века. На современных российских монетах также есть его изображение.<br>
	&nbsp;&nbsp;Двуглавого орла можно увидеть на первом российском рубле, выпущенном в виде монеты: он был отчеканен в 1654 году при царе Алексее Михайловиче. Рубли чеканились прямо на европейских серебряных талерах, которые содержали серебра не на 100 копеек, а только на 64. Вскоре чеканку неполноценных рублей прекратили. До наших дней сохранилось около 40 таких монет.
</p>
