<p>
	&nbsp;&nbsp;<a name="metka1"><b>Белый медведь</b></a>
</p>
<p>
	&nbsp;&nbsp;Белый медведь по праву считается символом Севера. Уши и хвост, мех и кожа, лапы и когти – все тело полярного медведя предназначено для жизни в Арктике, для защиты от холода и охоты на тюленей.<br>
	&nbsp;&nbsp;Изображение белого медведя можно увидеть на 25-рублевых банкнотах времен Гражданской войны. Эти красочные купюры использовали на севере России на территориях современных Архангельской и Мурманской областей. Банкноты в народе назывались «моржовками». Помимо белого медведя на них изображен морж, рыбы и ледяные торосы.<br>
	&nbsp;&nbsp;Также белый медведь представлен на памятных монетах Банка России серии «Сохраним наш мир». В 1997 году в этой серии вышло семь монет из золота и серебра, посвященных этому хищнику. Например, на золотой 100-рублевой монете весом 15,72 г изображен лежащий на льдине белый медведь. Эта монета отчеканена на Санкт-Петербургском монетном дворе.
</p>
