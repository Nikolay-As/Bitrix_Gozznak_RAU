	&nbsp;&nbsp;<a name="metka3"><b>Северный олень</b></a>
</p>
<p>
	&nbsp;&nbsp;Пожалуй, самыми известными в мире оленями являются олени Санта Клауса. Каждый год в ночь перед Рождеством они летают по небу и разносят подарки. Но северные олени помогают не только сказочному волшебнику. Многие столетия жители Крайнего Севера используют их в сельском хозяйстве и в качестве транспортного средства. Благодаря этим животным человек освоил отдаленные территории Севера. Каждый год северяне проводят гонки на оленьих упряжках. К Олимпиаде в Москве в 1980 году была выпущена серебряная десятирублевая монета с изображением соревнований на оленьих упряжках.<br>
	&nbsp;&nbsp;Еще семь монет с изображением северного оленя появилось в 2004 году в серии «Сохраним наш мир» - из золота и серебра. В частности, на 25-рублевой памятной монете фигура оленя представлена на фоне тундровой растительности. На заднем плане – два чума, человек и три собаки. Эта монета отчеканена на Московском монетном дворе, ее вес – 173,29 г.
</p>
