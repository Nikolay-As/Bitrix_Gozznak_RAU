<p>Гознак ведет свою историю с 1818 года, когда была создана Экспедиция заготовления государственных бумаг.</p>

<p>Этому предшествовала Отечественная война 1812 года, в ходе которой армия Наполеона наводнила Россию огромным количеством фальшивых ассигнаций (с 1813 по 1817 год на территории России было выявлено 5,6 миллиона поддельных рублей). Вопрос о необходимости создания особого учреждения для изготовления новых бумажных денег был поднят министром финансов Российской империи графом Дмитрием Александровичем Гурьевым. Руководить строительством нового предприятия поручили генерал-лейтенанту Августину Бетанкуру.</p>

<p>Днем рождения Экспедиция заготовления государственных бумаг стало 21 августа 1818 года (по старому стилю), когда император Александр I поставил на проекте учреждения Экспедиции резолюцию: «Быть по сему».</p>

<p>В результате был создан единый производственный комплекс, объединивший в себе все стадии производства денег – от отлива бумаги до разработки дизайна и последующей печати банкнот.</p>

<p>Экспедиция заготовления государственных бумаг быстро превратилась в передовое предприятие своего времени. Успехи Экспедиции были неоднократно отмечены международными премиями. Уже во второй половине XIX века на Всемирных выставках в Лондоне, Вене, Париже, в Копенгагене изделия Экспедиции награждались за техническое и художественное совершенство.</p>

<p>В начале XX века, в 1919 году, Экспедиция была реорганизована и получила название - Гознак. </p>

