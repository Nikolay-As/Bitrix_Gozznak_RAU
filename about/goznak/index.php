<p>
Сегодня акционерное общество «Гознак» - динамично развивающаяся современная компания, сохранившая и преумножившая лучшие традиции Экспедиции заготовления государственных бумаг и занимающая лидирующие позиции на российском и мировом рынках защитных технологий и решений. 
</p>
<p>
Единственным акционером АО «Гознак» является Российская Федерация в лице Федерального агентства по управлению государственным имуществом (Росимущество). Указом президента Российской Федерации АО «Гознак» внесено в перечень стратегических предприятий и стратегических акционерных обществ.
Производственные мощности, которыми располагает АО «Гознак», позволяют ежегодно производить десятки тысяч тонн защищенной банкнотной бумаги, миллиарды экземпляров банкнот и монет, десятки миллионов паспортов, десятки миллиардов экземпляров различных акцизных и специальных, а также почтовых марок. 
С момента создания компании ее уникальность – в возможности выполнять полный цикл работ, связанных с разработкой, выпуском и организацией обращения защищенной продукции.
</p>
<p>
Сегодня в состав АО «Гознак» входят восемь предприятий-филиалов: Московская печатная фабрика, Пермская печатная фабрика, Московская типография, Санкт-Петербургская бумажная фабрика, Краснокамская бумажная фабрика, Московский монетный двор, Санкт-Петербургский монетный двор и Научно-исследовательский институт.
</p>
<p>
В АО «Гознак» работают более 7 тысяч высокопрофессиональных специалистов, в числе которых – представители трудовых династий. 
</p>
<p>
В числе основных направлений деятельности АО «Гознак»:
</p>
<ul class="im-common-mb30">
	<li>Бумажное производство - начиная с 1818 года.</li>
	<li>Полиграфическое производство - начиная с 1818 года.</li>
	<li>Монетно-орденское производство - начиная с 1724 года.</li>
	<li>Услуги по обработке и гарантированному хранению электронных данных - начиная с 2014 года.</li>
	<li>Разработка систем контроля документов и банкнот в обращении – начиная с 2011 года.</li>
</ul>


<p>
В последние годы особое внимание АО «Гознак» уделяет внедрению цифровых технологий, занимаясь развитием Центров обработки данных, предоставляющих комплекс услуг как по размещению оборудования заказчиков, так и по выделению вычислительных ресурсов для развертывания информационных система.
Кроме того, одним из ключевых направлений деятельности АО «Гознак» является разработка  современных идентификационных документов, обеспечивающих идентификацию человека, в том числе – в электронной среде.
</p>

