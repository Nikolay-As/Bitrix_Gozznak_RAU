<p>
	<b>C 20 мая по 28 декабря 2022 года в залах Дома фондовых капиталов (Петропавловская крепость) работала&nbsp; выставка «Города на российских банкнотах и монетах», приуроченная к 25-летию современных российских банкнот.</b>
</p>
<p>
	Среди экспонатов выставки были нереализованные проекты банкнот XIX века, эскизы банкнот «городской серии», памятные монеты XIX, XX и XXI веков, а также другие предметы из коллекции АО «Гознак». Монеты и банкноты дополнены подлинными вещами XIX – XX веков, связанными с путешествиями: их предоставил Государственный музей истории Санкт-Петербурга.
</p>
<p>
	«Это уже наша шестая ежегодная выставка. В этом году она приурочена к 25-летию современных российских банкнот. Мы показываем, как в традициях банкнотного и монетного дизайна представлена наша страна. Причем мы демонстрируем не только денежные знаки, которые хорошо известны и которые мы видим в обращении. Мы показываем путь, который проходит банкнота с момента возникновения идеи. Это, безусловно, будет интересно и специалистам, и просто любителям российской истории», – отметил на открытии генеральный директор АО «Гознак» Аркадий Трачук.
</p>
<p>
	<b>На выставке посетители могли увидеть:</b>
</p>
<p>
	- эскизы кредитных билетов с видами городов Российской империи, выполненные в 1859 г.;
</p>
<p>
	- неосуществленные эскизы советских банкнот с видами Москвы и Ленинграда, среди которых – эскиз с изображением так и не построенной «сталинской высотки» в Зарядье;
</p>
<p>
	- утвержденные и неутвержденные эскизы банкнот «городской серии», выполненные в 1990-е годы;
</p>
<p>
	- утвержденные образцы современных российских банкнот;
</p>
<p>
	- открытка, на основе которой было создано изображение Большого театра на современной 100-рублевой купюре;
</p>
<p>
	- памятные монеты Российской империи, отчеканенные в XIX – начале XX в.;
</p>
<p>
	- редчайшие пробные монеты с видами Москвы 1962, 1967, 1976 годов;
</p>
<p>
	- пробные монеты 1990-х годов с изображениями архитектурных и скульптурных памятников России, которые рассматривались в качестве альтернативы монетам, находящимся в обороте в настоящее время;
</p>
<p>
	- несколько десятков серебряных и золотых памятных монет, посвященных городам и регионам России, и их эскизов;
</p>
<p>
	- памятная серебряная монета весом 1 кг, посвященная городам и территориям – участникам финального этапа общероссийского голосования по выбору символов для банкнот Банка России номиналами 200 и 2000 рублей.
</p>
<p>
	Многие из демонстрируемых на выставке предметов ранее никогда не выставлялись и не публиковались.
</p>
<p>
	По традиции к открытию выставки на Санкт-Петербургском монетном дворе – филиале АО «Гознак» были отчеканены памятные жетоны. Также издан буклет «Города на российских банкнотах и монетах».
</p>
<p>
	Выставка «Города на российских банкнотах и монетах» работала для посетителей с 20 мая по 28 декабря 2022 года.&nbsp;
</p>
