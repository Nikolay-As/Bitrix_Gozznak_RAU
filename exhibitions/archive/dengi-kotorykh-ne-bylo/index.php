<p>
	 В 2019 году исполнилось 250 лет российским бумажным деньгам. В 1769 году императрица Екатерина II подписала манифест об учреждении «государственных банков для вымена ассигнаций», тем самым положив начало их обращению в России. История российских бумажных денег весьма драматична и полна интересных фактов, она неотделима от экономической и политической истории страны.
</p>
<p>
	 Одна из основных целей выставки «Деньги, которых не было» – проследить историю банкнотного дизайна России, показав, как влияют на оформление банкноты художественные вкусы и тенденции, события в экономической и политической жизни страны. Кроме того, экспонаты выставки наглядно демонстрировали напряженную работу художников, технологов, печатников, предшествующую выпуску новой банкноты в свет.
</p>
<p>
 <strong>На выставке посетители могли увидеть:</strong>
</p>
<ul>
	<li>
	ассигнации 1802–1803 годов, тираж которых был отпечатан, но впоследствии уничтожен, в том числе – пробные, непронумерованные и неподписанные варианты; </li>
	<li>
	эскизы банкнот середины XIX века с портретами жителей разных губерний и видами городов тогдашней Российской империи – Санкт‑Петербурга, Москвы, Гельсингфорса и Варшавы; </li>
	<li>
	эскизы кредитных билетов работы Василия Верещагина, Михаила Микешина, Адольфа&nbsp;Шарлеманя; </li>
	<li>
	эскизы и оттиски банкнот «императорской серии» Рудольфа Рёсслера, на основе которых были выпущены самые знаменитые банкноты дореволюционной России – «катенька» и «петенька», в том числе – оттиски доски для металлографской печати, над которой гравер Перикл Ксидиас работал в течение нескольких лет; </li>
	<li>
	эскизы советских банкнот «пролетарского кредита» 1918 года с портретами Карла Маркса; </li>
	<li>
	авангардные эскизы и оттиски банкнот 1920-х годов, в т. ч. побывавшие на Всемирной выставке в Париже в 1925 году; </li>
	<li>
	эскизы и оттиски советских червонцев 1920–1930-х годов, в том числе – с видом Московского Кремля; </li>
	<li>
	эскизы банкнот 1930-х годов с портретом Владимира Ленина; с изображениями рабочих, красноармейцев, парашютистов, женщин; </li>
	<li>
	эскизы бумажных денег времен Великой Отечественной войны с портретами Александра Невского, Дмитрия Донского, Александра Суворова и Михаила Кутузова; </li>
	<li>
	эскизы советских банкнот 1967 года, посвященных 50-летию советской власти, с изображением атомного ледокола «Ленин» и космического корабля «Восток»; </li>
	<li>
	эскизы банкнот 1990-х годов с портретами Федора Шаляпина, Федора Достоевского, Антона Чехова, Михаила Ломоносова, Александра Пушкина, Дмитрия Менделеева, Василия Сурикова, Петра Чайковского и др.; </li>
	<li>
	первые варианты эскизов банкнот с изображениями видов российских городов, которые находятся в обращении до настоящего времени. </li>
</ul>
