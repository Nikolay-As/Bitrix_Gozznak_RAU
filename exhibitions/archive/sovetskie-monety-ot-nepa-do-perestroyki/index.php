<p>
	C 20 мая по 29 декабря 2021 года в залах Дома фондовых капиталов (Петропавловская крепость) проходила выставка выставки «Советские монеты: От НЭПа до перестройки». Эта выставка была посвящена истории советской монетной чеканке и приурочена к 100-летию начала чеканки первых общероссийских советских монет в 1921 году.
</p>
<p>
	 На выставке было представлено более 650 экспонатов, отражающих историю создания монет РСФСР и СССР: эскизы, пробные оттиски, утвержденные образцы монет и чеканочные инструменты. При этом 70% предметов ранее не демонстрировалось широкой публике.
</p>
<p>
	 Такое количество нумизматических редкостей советского периода до сих пор никогда не собиралось на одной выставочной площадке.
</p>
<p>
	 Выставка стала результатом длительной работы сотрудников выставочного комплекса АО «Гознак» по выявлению и изучению памятников советской нумизматики из собрания Гознака. На выставке были представлены предметы, которые ранее никогда не демонстрировались широкой публике, в том числе – легендарные монеты, которых нет в частных коллекциях.
</p>
 <b>
<p>
	 На выставке посетители могли увидеть:
</p>
 </b>
<p>
	 - все основные типы советских разменных монет;
</p>
<p>
	 - неосуществленные эскизы разменных монет, в том числе – выполненные художниками Е. Е. Лансере, С. Н. Грузенбергом, А. Ф. Васютинским, И. И. Дубасовым, Н. А. Соколовым, А. В. Козловым, И. С. Крылковым и другими художниками;
</p>
<p>
	 - неосуществленные эскизы монет, выполненные в Швейцарии для Советской России в 1922 году;
</p>
<p>
	 - инструменты для чеканки пробных и не выпущенных в обращение монет 1920 – 1950-х годов;
</p>
<p>
	 - комплекты пробных и не выпущенных в обращение монет 1947, 1956, 1958 – 1959, 1961 годов;
</p>
<p>
	 - эскизы памятных монет разных лет, в том числе – монет, посвященных городам и регионам СССР 1981 года, 60-летию монетной системы СССР 1984 года, Олимпийским играм в Сеуле 1988 года;
</p>
<p>
	 - пробные памятные монеты из драгоценных металлов: комплект серебряных монет, посвященных 50-летию Октябрьской революции; золотые монеты, посвященные 100-летию В. И. Ленина; золотые монеты «Горбачев. Перестройка. Гласность» 1990 года;
</p>
<p>
	 - более 40 ранее не известных пробных монет 1975 – 1977 годов, посвященных Московской олимпиаде 1980 года;
</p>
<p>
	 - сувенирную монету «1 рубль = 1 доллар», отчеканенная в 1988 году из металла советских ракет Р-12;
</p>
<p>
	 - эскизы последних монет СССР.
</p>
<p>
	 На выставке монеты и их проекты были показаны в контексте эпохи. Здесь можно было увидеть советские плакаты с изображениями монет, копилки и сберегательные книжки, газетные вырезки.
</p>
<p>
	 При оформлении выставки были использованы копии плакатов и фотографий, предоставленные Российской национальной библиотекой, Центральным государственным архивом Санкт-Петербурга и Российским государственным архивом кинофотодокументов.
</p>
<p>
	 Особый раздел выставки был посвящен деньгам в советской художественной литературе 1920–1930-х годов. Посетители увидят, в частности, какие монеты и банкноты могли использовать герои «Двенадцати стульев» и «Золотого теленка».
</p>
<p>
	 По традиции к открытию выставки на Санкт-Петербургском монетном дворе были отчеканены памятные жетоны и издана книга, посвященная истории советских монет.
</p>
