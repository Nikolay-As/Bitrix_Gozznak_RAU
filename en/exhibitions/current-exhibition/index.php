<p>
	 In 2020, the Russian money museum has prepared an exhibition "Victory Signs", dedicated to the 75<sup>th</sup> anniversary of Victory in the Great Patriotic War. The exhibition is placed in the halls of the House of Stock Capital on the territory of the Peter and Paul Fortress. It presents unique items from the collection of JSC "Goznak": banknotes, awards, postage stamps, government loan bonds of the Great Patriotic War. Among them are exhibits that have never been demonstrated before: in particular, prototypes of the Order of Suvorov, the highest military order established during the war.
</p>
<p>
	Due to restrictions imposed by the spread of coronavirus infection, now you can only visit the exhibition virtually.
</p>
<p>
	The first online tour of the exhibition was held on April 27, 2020. You can get acquainted with the exhibits presented at the exhibition here.
</p>
<p>
	The exhibition "Victory Signs" through the products of Goznak shows the history of the Great Patriotic War. Among the exhibits are sketches and trial versions of war awards; samples of government loan bonds approved by the People's Commissar of Finance A. G. Zverev; a sample medal "For the Victory over Germany in the Great Patriotic War of 1941 - 1945" approved in May 1945 by the Presidium of the Supreme Soviet of the USSR; sketches of paper money with portraits of outstanding military leaders of the past and ordinary war heroes - pilots, infantrymen, Red Fleet.
</p>
<p>
	For the first time the public is shown tools for minting medals and coins of the war years, as well as sketches of the Orders of Suvorov, Kutuzov, Alexander Nevsky, approved by Stalin; prototypes of the Order of Suvorov, made in Krasnokamsk in 1942; diplomas awarded the Order "Victory", made especially for Stalin and Brezhnev.
</p>
<p>
	 &nbsp;Also presented at the exhibition are approved samples of tickets for money and clothing lotteries of the war years, certificates of NKVD and military counterintelligence SMERSH, ration cards. The existence of some items previously was not known: for example, in preparation for the exhibition were found sketches of bonds of the Collective Farm and Cooperative Defense Loan of the country, which were developed by Goznak in autumn 1941.
</p>
<p>
	 &nbsp;A special section of the exhibition is dedicated to the memory of the war. Over the past three quarters of the century Goznak enterprises developed and produced commemorative coins, awards and commemorative medals, badges, postage stamps and envelopes, somehow related to the Great Patriotic War. For the first time at the exhibition there are sketches of anniversary medals and commemorative coins of the Soviet time, trial versions and approved samples of coins, including - samples of a medal dedicated to the 25<sup>th</sup> anniversary of Victory, which was not replicated, as the issue was considered too expensive.
</p>
<p>
	 &nbsp;There are also commemorative coins dedicated to the anniversary of the Victory, produced in modern Russia since 1995. In particular, gold and silver coins weighing 1 kg, as well as coins issued for the forthcoming 75<sup>th</sup> anniversary of the Victory are shown.
</p>
<p>
	 &nbsp;Separate stands are dedicated to the veterans of the Great Patriotic War - employees of Goznak.
</p>
