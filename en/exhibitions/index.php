<p>
	In addition to the main exposition, The History of Money, located in the Anninsky Cavalier building, 
	the Museum of the History of Money hosts temporary exhibitions. They are located in the exhibition halls of the House of Capital Assets.
</p>
<p>
	Traditionally, exhibitions open in the spring and work until the end of the calendar year. The first exhibition -
	<a href="https://museum.goznak.ru/en/exhibitions/archive-of-exhibitions/ot-kerenki-do-sovznaka/">“From the Kerenka to the Common Sign: Money and the Revolution”</a> 
	- was opened in 2017 and is dedicated to the history of money circulation in Russia in 1914 - 1922.
</p>
<p>
	In 2018, an exhibition was held at the House of Equity Capital 
	<a href="https://museum.goznak.ru/en/exhibitions/archive-of-exhibitions/1818/">«1818+. Two hundred years of Goznak history»</a> 
	dedicated to the anniversary of the Expedition for the procurement of government securities, from the moment of 
	creation of which Goznak has its history. It displayed such exhibits as samples of letters from heroic cities, 
	personal letters from Yu. A. Gagarin, G.K. Zhukov and others, samples of awards, banknotes, 
	sketches and test prints of postage stamps, medals of the 1980 Moscow Olympics and etc.
</p>
<p>
	The theme of the 2019 exhibition is unrealized paper money projects. In 2019, 
	the 250th anniversary of the first banknotes in Russia is celebrated, and the exhibition is dedicated to this event
	<a href="https://museum.goznak.ru/en/exhibitions/unissued-money/">«Unissued money»</a>. 
	It shows the history of the design of Russian banknotes over the past two hundred years. 
	Among the exhibits are sketches and test prints of paper money, which are shown for the first time at the exhibition.
</p>
<p>
	In 2020, it is planned to open a new exhibition dedicated to the 75th anniversary of the Victory in the Great Patriotic War of 1941-1945.
</p>
