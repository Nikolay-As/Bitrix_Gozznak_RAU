<p>In addition, visitors could see sketches of illustrations by Ivan Bilibin to Russian fairy tales, which were issued by the Expedition, illustrated calendars and the so-called ceremonial editions of the Expedition in the late XIX - early XX centuries.
</p>
<p>
A special section of the exhibition was devoted to "space" products of Goznak. Here one could see space pennants, a sample of the USSR pilot-cosmonaut diploma presented to Yuri Gagarin, and an invitation to the demonstration on the Red Square dedicated to the first human space flight of April 14, 1961.
</p>
<p>
The exhibition also reflected the modern stage of Goznak JSC work. In particular, it was possible to see here a new commemorative banknote of 100 rubles face value dedicated to the World Cup-2018. This is the first Russian banknote issued on a polymer substrate.
</p>
<p>
Exhibition "1818+. Two hundred years of Goznak'" complemented the main exposition "History of Money", which Goznak opened in 2016 in the building of Cavalier of the Peter and Paul Fortress.
</p>
<p>
The exhibition could be visited by ticket to the main exposition. The work schedule of the exhibition also coincided with the work schedule of the main exposition (from 10.00 to 20.00; till the end of September - without days off; from October to the end of December - every day except Thursday).
</p>
<p>
It was possible to get acquainted with the exhibition by yourself or to visit excursions (excursions around the exhibition were held daily at 17.00).
</p>

