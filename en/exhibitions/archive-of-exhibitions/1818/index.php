	From May 24 to December 30, 2018 in the House of Stock Capital on the territory of the Peter and Paul Fortress worked the exhibition "1818 +. Two hundred years of the history of Goznak", opened to the anniversary of the company, which traces its history since the founding of the Expedition of storing state papers  in 1818.
</p>
<p>
	The basis of the exhibition was formed by items from the funds of Goznak, many of which had not been previously shown to the general public. For two centuries the Expedition of storing state papers and Goznak issued not only banknotes and coins but also books and orders, calendars and postage stamps, letters of commendation and space pennants, passports and even business cards of the country's leaders.
</p>
<p>
	At the exhibition were presented the first Russian paper money and samples of banknote paper; money and payment stamps for Poland, Finland and Russian America 1820 - 1850s; samples of banknotes, bonds, stamps with autographs of Emperors Alexander II and Nicholas II, Sergey Stolypin; approved samples of the first national postal stamp of the Russian Empire (1857) and the first postal stamp of the RSFSR (1918), sketches and essays of postal stamps by Richard Zarrin, Sergei Chekhonin, Nathan Altman, etc.; diploma of the Hero-City of Leningrad; awards medals of the Moscow Olympics in 1980.
</p>
<p>
	One of the unique items shown at the exhibition is a miniature (only 28 by 22 mm.) edition of Ivan Krylov's fables, issued in 1856.
</p>
