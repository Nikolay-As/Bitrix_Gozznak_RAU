	The first scientific conference "Money in Russian history. Issues of production, circulation,
	and existence" was held on October 18-19, 2018 in the House of Stock Capital of the Peter and Paul Fortress. The program 
	included six thematic sections: "History of coin production", "History of banknote production: technologies and artistic design", "Treasures: 
	problems of attribution, description, preservation", "Issues of history of circulation of coins and banknotes", "Written sources and history 
	of study of coins and banknotes", "Monetary circulation in the former Russian Empire in 1918 - 1922".
	The conference was attended by over fifty specialists from Russia, Belarus, Sweden, Slovakia and Latvia. Among the Russian participants 
	there were representatives from St. Petersburg, Moscow, Tver, Kursk, Novosibirsk, Yekaterinburg, Tula, and Yaroslavl. The participants 
	and guests of the conference were employees of the largest scientific centers of Russia: the State Hermitage Museum and the State Historical 
	Museum, the Institute of Archaeology of the Russian Academy of Sciences, the Institute of History of the Russian Academy of Sciences, the 
	Higher School of Economics, the Pushkin State Museum of Fine Arts, St. Petersburg State University, the Russian National Library, regional local 
	history museums and other organizations. Especially for the conference, the Saint-Petersburg Mint, a branch of Goznak JSC, minted a commemorative 
	token in two versions - in melchior and tompak. The first tokens are designed for speakers, the second - for section chairs and colleagues who actively 
	contributed to the conference. The design of the token is based on the sketch of the medal "On correction of the coin business", made by the scientist 
	and inventor Andrey Andreyevich Nartov in the 1770s. Each token has a unique number.
	A collection of materials "Money in Russian History. Issues of production, circulation, existence". It is available 
	for purchase in the Museum Shop, located in the Golovkin Bastion of the Peter and Paul Fortress.
