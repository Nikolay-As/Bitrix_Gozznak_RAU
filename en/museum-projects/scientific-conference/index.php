	The second international scientific conference "Money in Russian history: issues of production, circulation, existence"
	was held in the Museum of Money History of Goznak JSC on October 16-18, 2019. This year, apart from the plenary session, the
	conference program included thematic sections: "Production and circulation of coins" (in different historical periods), "Production and 
	circulation of paper money" (in different historical periods), "Monetary circulation 
	in the territory of the Russian Empire in 1914 - 1922", "Treasures and coins in archaeological monuments", "Monuments of numismatics, bonistics, 
	faleristics in museum collections and the history of their study", "Money in written sources and fiction". The conference was attended by over seventy specialists, 
	including representatives of the State Hermitage Museum, the State Russian Museum, the Yaroslavl Museum Reserve, the Institute of Archaeology of Crimea, 
	the Institute of Archaeology of the Slovak Academy of Sciences and many others.
	<br><br>
	By the beginning of the conference, the St. Petersburg Mint has traditionally issued commemorative 
	tokens with individual numbers in "silver" and "gold" versions. The "silver" token was intended for the speakers, 
	the "gold" token for the leading sections and organizers; some tokens were received by the museum collections.
