<h2>Thematical sections of the museum </h2>

<h3>1. Money of Ancient Russia</h3>

<p>The museum collection shows the history of the first coins circulated on the territory of ancient Russia. The first Russian coins minted in the country are on display. Spindle whorls, shells  and silver ingots will tell about the mysterious "coinless period" of the 12 - 14th centuries.</p>

<h3>2. Currency circulation within the territory of the Russian princedoms at the end of the 14th - the beginning of the 16th centuries</h3>

<p>Coins of the Moscow princes Dmitry and Ivan III; coins from Ryazan, Tver, Novgorod and other Russian cities of the 15th century demonstrate the revival of coin issuing in the 14 – 15th centuries. The minting technology of Russian coins of that time will explain their unusual oblong shape.</p>

<h3>3. Currency circulation of the Muscovite state in the early 16th - 17th centuries</h3>

<p>In the 1530s, as a result of the currency circulation unification in the Muscovite state, there appeared a new coin – a silver kopek. The museum obtains a collection of silver coins with the names of the Russian tsars of three different dynasties - Ivan IV the Terrible, Boris Godunov, Mikhail Romanov. Coins of False Dmitry I, Vasily Shuisky and Polish Prince Vladislav coinside the Time of Trouble. There are coins of Tsar Alexey Mikhailovich that caused the famous "Copper revolt" of 1662.</p>

<h3>4.Reforms of the monetary system and coin minting during the reign of Peter the Great (1698 - 1725)</h3>

<p>Peter the Great altered the Russian coinage radically - copper, silver and gold coins with different face values were struck on a large scale at a few mints, including the Saint-Petersburg Mint founded in 1724. During the reforms of Peter the Great, the rouble acquired its tangible embodiment and ceased to be a conditional unit of account.</p>

<h3>5. Currency circulation in the 18th century (1725 - 1801)</h3>

<p>The silver and gold coins of the 18th century introduce a gallery of portraits of emperors and empresses and are often masterpieces of the medallic art of the time. The history and the technology of mass recoining, the appearance of paper money – "assignations" in Russia at the end of the 18th century as well as Russian coins for East Prussia and Poland are exhibited.</p>

<p>The visitors can gain a glimpse into a mint and a paper mill of the 18th century; their operation is reconstructed according to the unique documents.</p>

<h3>6. Currency circulation in the 19th - early 20th centuries (1801 - 1917)</h3>

<p>The visitors will see the rare and unique currency units of the Russian Empire, including the first platinum coins. First proffessional banknotes issued by the Expedition of Storing State Papers (Goznak since 1919) are on display. Coins minted for the national outskirts of the Russian Empire - Georgia, Poland and Finland are exhibited also.</p>

<h3>7. Currency circulation during the revolution and the Civil war (1917 - 1924)</h3>

<p>The Civil war caused economical crises and money depreciation. Money were issued by different local authorities and as a result the country was flooded with various types of banknotes. Thousands of paper currency units were in circulation at once including credit notes of the Provisional Government, the RSFSR (Russian Soviet Federated Socialist Republic), the White Governments and  various cash equivalents as well.</p>

<h3>8. Currency circulation in the RSFSR and the USSR (1921 - 1991)</h3>

<p>The visitors will find out how the monetary system was stabilized after the end of the Civil war and the new "hard" money were introduced. The visitors will see an extremely rare gold chervonets of 1925. The first Soviet coins minted of the new alloys on the basis of copper and nickel; the Soviet paper money of the 1920s-1940s - masterpieces of the printing art; money in circulation during the Great Patriotic war are exhibited. </p>

<h3>9. The modern period (from 1991 to the present day)</h3>

<p>The beginning of the 1990s was the time of increasing inflation. The visitors will see the first money of the new Russia issued in 1992 as well as the first banknotes of the so called "city series" that were introduced in the middle of the 1990s and are still issued today.</p>

<h3>10. The history of the enterprises of JSC Goznak</h3>

<p>Goznak consolidates a few manufacturing enterprises with a long and rich history. One can flip through the virtual pages of history of each of them - video films will tell about how the modern mints, paper mills and printing factories are operating today.</p>

<h3>11. Money and art</h3>

<p>Currency units have always been considered works of art; artists of various epochs have worked upon their design. Their craftsmanship is illustrated with unaccomplished projects, trial and commemorative coins and banknotes as well as sketches, photographs, punches used in their design and manufacture.</p>

<h3>12. Counterfeited money</h3>

<p>Money counterfeiting has existed for as long as the money itself, and has been against the law since that time.  Counterfeited coins, banknotes and equipment for their production  help to trace the evolution of counterfeiters' activity during the last two centuries.</p>

