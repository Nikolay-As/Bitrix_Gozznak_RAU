<h2 id="etno">
	Coin adornments
</h2>
<p>
	Now and always coins made of precious metal were the symbol of prosperity.
	They reflected social, confessional and financial status of people. They also served for ritual and mystical purpose.
	In traditional cultures of ethnic minorities of Russia coins were decorating clothes and tableware.
	In the second half of 19th century instead of chests and jewelry boxes people start to use exquisite coin trays and wallets.
	Very often they were made of precious metal therefore immediately got popularity.
</p>

