			During the war with Napoleon in 1812 Russian economics was unbalanced by huge amount of counterfeited banknotes issued by French.
			Alexander I clearly understood that it was high time to use and develop modern technologies to protect financial system of the country.
			The history of modern Goznak enterprise goes back to the paper mill (Expedition of paper production) founded by Alexander I in 1818 in St.Petersburg.
