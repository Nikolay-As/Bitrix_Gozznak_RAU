<h3>
	Tsar Aleksey Romanov (1645-1676)
</h3>
<p class="is-small">
	One ruble. 1654. Silver. Moscow mint.
</p>
<p>
	Until the middle of the 17th century ruble was neither a coin but a unit of weight and account.
	Only in 1654 a coin with a name of ruble was minted during the reign of Aleksey Romanov (father of Peter the Great).
	It was remade out of thaler – European silver coin. Thalers were wide spread in Russia since 16th century. Only 40 original coins are known today.
</p>
