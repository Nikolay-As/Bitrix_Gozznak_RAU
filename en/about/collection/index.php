	<div class="is-col is-60">
		<p>
			By the middle of 18th century copper coins became the basement of Russian money circulation.
			However copper was not a universal metal and very often it was complicated to use it for payments.
			According to the legend one of the Russian poets was granted with 2000 rubles for the poem.
			This sum of money was payed him with copper coins Several drays were loaded with two tons of copper coins.
			Very soon copper coins lost their purchasing capacity. The government of Katherine II made a decision to issue bonds.
			First banknotes of 1769 were printed on a thin large list of paper without any artistic design.
			Only text on them proclaimed that copper coins should be exchanged against new paper banknotes.
			Banknotes were issued by Moscow and St.Pb. banks and had the signatures of the directors.
			The nominal value of these first banknotes was 25, 50 ,75 and 100 rubles.  Museum collection has the banknotes of 25 and 100 rubles.
		</p>
	</div>
</div>

