<h3>
	Peter I (1682-1725, emperor since 1721)
</h3>
<p class="is-small">
	Ruble 1724.Silver.St. Petersburg Mint (in Berg-collegium building).Engraver: Anton Schultz (?).Diameter: 43,5 mm.Weight: 28,31 g.Edge: reeded
</p>
<p>
	Peter the Great had finally reformed Russian monetary system in the beginning of the 18th century.
	He was the first Russian tsar ( in 1721 got the title of emperor) who ever left Russia and travelled abroad.
	He was looking for new modern technologies. He had been to the London mint and probably met the headquarters of it – Isaac Newton.
	Coins got the proper round shape and edge. He totally updated mint manufacturing.
	Manual coinage was left behind by mechanical one. The foundation of St.Petersburg Mint on the territory of Peter and Paul fortress in 1724 was a breakthrough in coin production.
	St.Pb. Mint was supplied with up to date equipment and very soon turned to be one of the best in Europe. It is considered to be the oldest enterprise of Goznak.
	Ruble of 1724 is one of the first minted in Peter and Paul fortress.
</p>
