			Full cycle of banknote issuring was carried out there – from paper manufacturing till printing.  Paper mill was using steam machines.
			Watermarks became essential part of paper money also.
			Technologies of banknote production were constantly improved due to the breakthroughs and inventions of many scientists who collaborated with Expedition.
