<p>
	 Today Goznak successfully competes in the Russian and international markets of security product manufacturers.
</p>
<p>
	 Goznak consists of 8 enterprises: the Moscow Printing Factory, the Perm Printing Factory, the Moscow Printing House, the Saint Petersburg Paper Mill, the Krasnokamsk Paper Mill, the Moscow Mint, the Saint Petersburg Mint and the Scientific Research Institute.
</p>
<p>
	 Goznak sucessfully carries out the whole cycle of banknote and secured paper production as well as production of token and commemorative coins, state awards of the Russian Federation, security papers and ID documents.
</p>
<p>
	 In 2016, the procedure of reorganizing of Federal State Unitary Enterprise “Goznak” into Joint Stock Company “Goznak” with 100% of shares remaining the state property, was accomplished.
</p>
<p>
	Goznak JSC employs over 7 thousand highly professional specialists, including representatives of labor dynasties.
</p>
<p>
	Among the main activities of Goznak JSC:
</p>
<ul class="im-common-mb30">
	<li>Paper production - since 1818.</li>
	<li>Polygraphic production - since 1818.</li>
	<li>Coin and award production - since 1724.</li>
	<li>Services in processing and guaranteed storage of electronic data - since 2014.</li>
	<li>Development of control systems for documents and banknotes in circulation - starting from 2011.</li>
</ul>
<p>
	>In recent years, Goznak JSC has paid special attention to the introduction of digital technologies, developing data processing centres providing a range of services both for placing customers' equipment and for allocating computing resources for deploying information systems. Besides, one of the key directions of Goznak JSC activity is development of modern identification documents providing identification of a person, including - in electronic environment.
</p>


