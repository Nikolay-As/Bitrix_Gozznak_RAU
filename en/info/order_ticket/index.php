<p>
	A visit to the “History of Money” exposition is possible, both as part of organized groups, and individually (self-examination or with excursion service).
</p>
<p>
	Tickets to the Museum of the History of Money can be purchased by paying with a bank card or for cash at the box office of the museum 
	(it is located in the lobby of the Anninsky Cavalier). There is also a ticket terminal near the museum entrance where you can buy 
	entrance tickets (only for cash; preferential tickets are not sold in the terminal).
</p>
<p>
	The ticket office is open from 10:00 to 19:30.
</p>
<p>
	Please note that from October to April the museum is open daily except Thursday, and from May to September - seven days a week.
</p>
<p>
	The entrance ticket provides the opportunity for a single visit to the main exposition "History of Money" in the Anninsky cavalier.
</p>
<p>
	Please keep your tickets until the end of your visit.
</p>
<h2>Ticket price:</h2>
<h3>Entrance ticket (without excursion service):</h3>
<ul>
	<li>full ticket - 200 rubles;</li>
	<li>preferential ticket for schoolchildren, students and senior citizens (upon presentation of the relevant documents) - 100 rubles;</li>
	<li>family ticket (one / two adults + one / two children under 16) - 400 rubles;</li>
</ul>
<h3>Entrance ticket (with excursion service <strong>scheduled</strong>):</h3>
<ul>
	<li>full ticket - 250 rubles;</li>
	<li>preferential ticket for children over 7 years old, students, pensioners (upon presentation of the relevant documents) - 150 rubles;</li>
	<li>family ticket (one / two adults + one / two children under 16) - 500 rubles;</li>
	<li>ticket for excursion services on a schedule (subject to the purchase of an entrance ticket) - 50 rubles.</li>
</ul>
<p>
	Personal excursion service for organized groups outside the schedule (by reservation) 
	up to 20 people (inclusive) - 2000 rubles per group plus entrance tickets for all group members.
</p>
<p>
	Special conditions - for groups of students of secondary and secondary special education institutions 
	(from 1 to 15 people), cost - 1000 rubles per group plus entrance fees for all group members.
</p>
