<p>
	 Дизайн жетонов был разработан ведущим художником Дизайнерского центра АО «Гознак» Еленой Крамской , чеканку осуществил Санкт-Петербургский монетный двор.
</p>
<p>
	 В первую серию вошли жетоны с изображениями денги великого князя Дмитрия Ивановича Донского, первой рублевой российской монеты – серебряного рубля 1654 года, двойного червонца 1714 года, 10 рублей 1757 года, донативной монеты достоинством 37 рублей 50 копеек – 100 франков 1902 года, а также пробных и не выпущенных в обращение советских монет – 20 копеек 1934 года, червонца 1925 года,&nbsp; ½ копейки 1961 года, 50 копеек 1962 года и 10 рублей 1970 года.
</p>
<p>
	 Жетоны отчеканены в сплаве того же цвета, что и монеты, которым они посвящены: четыре из них выполнены в белом металле, шесть – в желтом.
</p>
<p>
	 Тираж жетонов – по 500 штук каждого вида.
</p>
<div>
 <b>В 2019 году были выпущены пять новых видов жетонов&nbsp;</b><b>"Редкости Музея истории денег".<br>
 </b><br>
</div>
<p>
	 Монеты для четырех из них были определены с помощью народного голосования: с 29 декабря 2018 года по 31 января 2019 года каждый желающий мог выбрать свою четвертку фаворитов из 14 монет, представленных на музейном сайте. Выбор пятой монеты (для пятого жетона 2019 года) сотрудники Музея истории денег оставили за собой.
</p>
<p>
	 В голосовании приняли участие более двухсот человек. Наибольшее число голосов собрали монеты:
</p>
<p>
	 – севский чех 1686 года,
</p>
<p>
	 – серебряный полуполтинник царя Алексея Михайловича 1654 года,
</p>
<p>
	 – пробный советский рубль, выпущенный в 1962 году,
</p>
<p>
	 – псковская серебряная денга XV века с изображением покровителя города – князя Довмонта.
</p>
<p>
	 По решению сотрудников Музея истории денег, на пятом коллекционном жетоне изображен серебряный «таврический» гривенник 1787 года.
</p>
<p>
	 Новые жетоны отчеканены в мельхиоре, поскольку все монеты, которым они посвящены, также отчеканены в белом металле (в серебре и медно-никелевом сплаве).
</p>
<p>
	 Диаметр жетонов – 35 мм, качество – «пруф», тираж – по 500 штук каждого наименования.<br>
</p>
