<p>
 <b>&nbsp;&nbsp;Основные этапы мастер-класса:<br>
 </b>
</p>
<p>
	 &nbsp;&nbsp;– <u>Процесс размола бумажной массы.</u> Он представлен в демонстрационном режиме на примере малого ролла. Размол бумажной массы, используемой на мастер-классе, выполняется заранее на большом ролле: это длительный и шумный процесс.<br>
	 &nbsp;&nbsp;– <u>Вычерпывание бумажной массы из чана.</u> Производится при помощи специальных рамок с плетеными проволочными сетками (рисунок плетения формирует текстуру водяного знака на листе бумаги).<br>
	 &nbsp;&nbsp;– <u>Формирование на горке стопы из получившихся листов.</u> Осевшая на сетке масса – будущий бумажный лист – скидывается на специальную деревянную горку. Листы прокладываются между собой сукном для оптимального отвода влаги и во избежание слипания.<br>
	 &nbsp;&nbsp;– <u>Отжим.</u> Получившаяся стопа листов перемещается на площадку винтового пресса и отжимается вручную для удаления излишком воды.<br>
	 &nbsp;&nbsp;– <u>Окончательная сушка бумаги.</u> Выполняется на специальном сушильном цилиндре. Влажные листы бумаги раскладываются на сушильной сетке и заводятся за нагретый цилиндр, где в течение нескольких минут обеспечивается их полное высушивание.<br>
	 &nbsp;&nbsp;Участники мастер-класса лично участвуют во всех операциях по изготовлению бумаги методом ручного отлива, и в итоге каждый получает готовый лист бумаги.<br>
	 &nbsp;&nbsp;В рамках проекта по созданию мастер-класса АО «Гознак» выполнило работы по реставрации казематов западной части Кронверкской куртины Петропавловской крепости – с консервацией кирпичной кладки и фрагментов обнаруженной исторической штукатурки, а также музеефикацией участков цокольного этажа.<br>
</p>
